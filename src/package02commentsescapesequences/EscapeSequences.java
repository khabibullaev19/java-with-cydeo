package package02commentsescapesequences;

public class EscapeSequences {

    public static void main(String[] args) {

        // Escape sequences are used to write a series of special characters
        // Each escape sequence character is preceded by a backslash "\"

        // "\n" -> newline => Advances the cursor to the next line for subsequent printing.
        System.out.println("Hello World!\nHello Cydeo!");

        // "\t" -> Horizontal tab => Causes the cursor to skip over to the next tab stop.
        System.out.println("\tJava Programming Language!");

        // "\\" -> Backslash => Causes a backslash to be printed.
        System.out.println("\\");

        // " \" " -> double quote => Causes a double quotation mark to be printed.
        System.out.println("I like \"Wooden Spoon\"");

    }

}
