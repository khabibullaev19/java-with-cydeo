package package03variables;

public class VariableNamingRules {

    public static void main(String[] args) {

        int age = 19;
        System.out.println(age);

        double stateTax = 5.6;
        System.out.println(stateTax);

        double federal_tax = 28.5;
        System.out.println(federal_tax);

    }

}
