package package04primitivecastingsconcatenation;

public class StringConcatenation {

    public static void main(String[] args) {

        /*
            -> The action of linking things together by using "+" operator.
            -> There must be a String of text on the right or left side of the "+" operator.
         */

        String name = "James";
        int age = 45;
        String from = "England";

        System.out.println(name + " is from " + from);
        System.out.println(name + " is " + age + " years old.");

    }

}
