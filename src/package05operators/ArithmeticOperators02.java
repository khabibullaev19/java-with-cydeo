package package05operators;

public class ArithmeticOperators02 {

    public static void main(String[] args) {

        /*
                In Math:
                    20 / 3 = 6.6666...

                    remainder = numerator - (denominator * integer result)

                    remainder = 20 - (3 * 6)
                    remainder = 20 - 18
                    remainder = 2

                In Java:
                    Remainder = 20 % 3
                    Remainder = 2
         */

        System.out.println(40 % 9);
        System.out.println(10 % 2);

    }

}
