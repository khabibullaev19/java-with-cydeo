package package05operators;

public class RelationalOperators02 {

    public static void main(String[] args) {

        System.out.println(100 == 100);
        System.out.println('A' == 'a');

        System.out.println(100 != 200);
        System.out.println(100 != 100);

        System.out.println(true != false);

    }

}
