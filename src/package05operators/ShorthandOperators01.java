package package05operators;

public class ShorthandOperators01 {

    public static void main(String[] args) {

        int x = 100;
        System.out.println(x);

        x = 200;
        System.out.println(x);

        int salary = 10000;

        salary += 10000;
        System.out.println(salary);

        int y = 2000;

        y -= 200;
        System.out.println(y);

    }

}
