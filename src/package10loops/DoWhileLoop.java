package package10loops;

public class DoWhileLoop {

    public static void main(String[] args) {

        int num = 10;

        while (num > 1000) {
            System.out.println("Hello World");
        }

        System.out.println("------------");

        do {
            System.out.println("Hello World");
        }while (num > 1000);

    }

}
