package package12arraysmultidimensionalarrays;

public class ArraysIntro {

    // Special type of variable that can hold multiple values of same data types.
    // Supports both primitive and non-primitive data types.
    // Has a unique index number for each element.
    // Size is fixed.

    // We can put brackets "[]" after the data type or after the variable name(array name)
    // -> int[] scores;          int scores[];

    // To instantiate one dimensional array, use the keyword "new", followed by data type
    // and then finally specify the size within square brackets "[]".
    // -> int[] scores;             int[] scores = new int[4];
    // -> scores = new int[4];      String[] names = new String[5];

    // Elements of an array can be accessed by using the square brackets[]
    // Index number need to be provided within the square brackets.

}
