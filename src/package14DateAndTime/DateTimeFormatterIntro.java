package package14DateAndTime;

public class DateTimeFormatterIntro {

    public static void main(String[] args) {

        // A class that provides the ability to format LocalDate, LocalTime, and LocalDateTime.
        // Presented in java.time.format package.

        // Date:
        // y => 4 digit year number
        // yy => 2 digit year number
        // M => Months number
        // MM => Months number (ignores 0 for number of 0~9)
        // MMM => Months abbreviation (Jan, Feb, Mar...etc)
        // MMMM => Month name (January, February, March...etc)
        // d => Day number
        // dd => Day number (includes 0 for numbers of 0~9)
        // E => Days of week abbreviation (Mon, Tue,..etc)
        // EEEE => Day name (Monday, Tuesday,...etc)

        // Time:
        // H => 24-hour format
        // HH => 24-hour format(includes 0 for number 0~9)
        // h => 12-hour format
        // hh => 12-hour format(includes 0 for number 0~9)
        // m => Minute format
        // mm => Minute format(includes 0 for number 0~9)
        // s => second format
        // ss => second format (includes 0 for number 0~9)
        // a => AM/PM

    }

}
