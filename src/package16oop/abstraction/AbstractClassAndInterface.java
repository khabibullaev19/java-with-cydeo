package package16oop.abstraction;

public class AbstractClassAndInterface {

    /*

        Abstract class:
        - Can not be instantiated.
        - Multiple inheritance is not allowed.
        - Can have constructor.
        - Can have instance, static, and abstract methods.
        - Can have instance and static variables.
        - Can not be final.
        - Can use all 4 access modifiers.

        Interface:
        - Can not be instantiated.
        - Multiple inheritance is allowed.
        - Can not have constructor.
        - Can have static, abstract and default methods.
        - Can only have static variables(final by default).
        - Can not be final.
        - Can only public & private(static methods) access modifiers.

     */

}
