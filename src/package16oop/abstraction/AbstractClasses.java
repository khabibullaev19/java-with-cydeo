package package16oop.abstraction;

public class AbstractClasses {

    /*

        - A class that's meant to be a super(parent) class.
        - The abstract keyword is used to create the abstract class.
        - The abstract class can not be instantiated(creating object).

        - An abstract class is meant to be inherited only,
          not meant to be instantiated(creating object).

        - A subclass of abstract class is called concrete class,
          and it can be instantiated.

        - A Concrete class must implement all the inherited abstract methods.

     */

}
