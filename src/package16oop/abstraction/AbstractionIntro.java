package package16oop.abstraction;

public class AbstractionIntro {

    /*

        - Process of hiding the implementation details of functions from the user.
        - Only the functionality will be provided to the user.
        - User will have the information on what the object does instead of how it does.


        - Abstract Method:
            -> A method that does not have body.
            -> A method that's meant to be overridden.
            -> The abstract keyword is used to create an abstract method.

            Abstract method can not have body.
            Abstract method can not be static.
            Abstract method can not be final.
            Abstract method can not be private.
            Abstract method can only be declared in an abstract class or in an interface.

     */

}
