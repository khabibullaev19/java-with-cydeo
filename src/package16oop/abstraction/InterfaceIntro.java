package package16oop.abstraction;

public class InterfaceIntro {

    /*

        - A blueprint of a class.
        - Specifies the behaviors that class(es) needs to implement.
        - Used to provide additional methods that the class(es) need.
        - The interface keyword is used to create the interface.

        - Variables are static & final by default.
        - An Interface can have static methods, abstract methods, and default methods only.
        - The Abstract keyword is given by default to the abstract methods of the interface.
        - The public access modifier is given by default to all the variables and methods.

        Variables => static and final variables ONLY.
        Methods => static, default, and abstract methods ONLY.

        -> An interface can not have instance variables.
        -> An interface can not have instance methods.
        -> An interface can not have constructors.
        -> An interface can not have initializer blocks.

        -> A class can inherit multiple interfaces at the same time.
        -> The implements keyword is used to inherit from the interface(s).
        -> The class must implement(override) all the abstract methods of the interface.

     */

}
