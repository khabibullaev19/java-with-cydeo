package package16oop.inheritance;

public class FinalKeyword {

    /*

        - Used to restrict the user
        - Makes the features unchangeable
        - Final keyword is applicable to:
                    Variables
                    Methods
                    Classes

        - Variables:
                    The value of the variable becomes unchangeable
                    Can not be reassigned

        - Methods:
                  Implementation of the methods becomes unchangeable
                  Can not be overridden

        - Classes:
                  A class that can not be extended
                  The class can not be a super class

     */

}
