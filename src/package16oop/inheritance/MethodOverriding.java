package package16oop.inheritance;

public class MethodOverriding {

    /*

        - Giving different implementations to the method.
        - Must take place in a subclass.
        - Less memory usage and improves the reusability.
        - Access modifier of the overridden method must be
          same or more visible.
        - Return type, method name and parameters of the
          overridden method must be same.
        - Can not override methods with private access
          modifier, static keyword, and final keyword.

     */

}
