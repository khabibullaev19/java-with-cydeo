package package16oop.polymorphism;

public class ReferenceTypeCastings {

    /*

        -> Casting one reference type to another.
        -> There must be IS A(inheritance) relation
           between the object type and reference type
           we are casting it to.
        -> There are two types of reference type castings:
                Upcasting (child -> parent)
                Downcasting (parent -> child)

        Upcasting:
            -> Casting the smaller reference type(sub-type) to a larger
               reference type(super-type)
            -> Done implicitly, the cast operator is not required
               to be given explicitly.

        Downcasting:
            -> Casting the larger reference type(super-type) to a smaller reference type(sub-type).
            -> Done explicitly, the cast operator is required to be given explicitly.
            -> Allows us to access the features of the object's type that are not in reference type.

     */

}
